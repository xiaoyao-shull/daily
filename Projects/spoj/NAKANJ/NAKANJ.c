struct point_t {
  int x;
  int y;
};
typedef struct point_t point_t;

struct deque_t {
  int capacity;
  int head;
  int tail;
  point_t * data;
};

typedef struct deque_t * deque_t;

deque_t deque_make(int capacity) {
  struct deque_t * ptr = malloc(sizeof(*ptr) + sizeof(point_t) * (capacity + 1));
  ptr->capacity = capacity;
  ptr->head = 0;
  ptr->tail = 0;
  ptr->data = (point_t *) (ptr + 1);
  return ptr;
}

void deque_push_back(deque_t deque, point_t value) {
  deque->data[deque->tail] = value;
  deque->tail++;
  if (deque->tail == (deque->capacity + 1)) {
    deque->tail = 0;
  }
}

point_t deque_pop_front(deque_t deque) {
  point_t value = deque->data[deque->head];
  deque->head++;
  if (deque->head == (deque->capacity + 1)) {
    deque->head = 0;
  }
  return value;
}

int deque_size(deque_t deque) {
  if (deque->tail > deque->head) {
    return deque->tail - deque->head;

  } else {
    return deque->tail - deque->head + (deque->capacity + 1);
  }
}

void deque_clear(deque_t deque) {
  deque->head = 0;
  deque->tail = 0;
}

int dx[8] = { -2, -1, 1, 2, 2, 1, -1, -2 };
int dy[8] = { 1, 2, 2, 1, -1, -2, -2, -1 };
int visited[8 * 8];
deque_t deque;

void clear_visited() {
  memset(visited, 0, sizeof(visited));
}

void set_visited(point_t p) {
  visited[p.x * 8 + p.y] = 1;
}

int is_visited(point_t p) {
  return visited[p.x * 8 + p.y];
}

int is_valid(point_t p) {
  return p.x >= 0 && p.x < 8 && p.y >= 0 && p.y < 8;
}

int run(point_t s, point_t t) {
  point_t p, q;
  int step = 0;
  int i, j, k;
  clear_visited();
  deque_clear(deque);
  set_visited(s);
  deque_push_back(deque, s);
  while (deque_size(deque) > 0 && !is_visited(t)) {
    step++;
    for (i = 0, j = deque_size(deque); i < j; ++i) {
      p = deque_pop_front(deque);
      for (k = 0; k < 8; ++k) {
        q.x = p.x + dx[k];
        q.y = p.y + dy[k];
        if (!is_valid(q) || is_visited(q)) {
          continue;
        }
        set_visited(q);
        deque_push_back(deque, q);
      }
    }
  }
  return step;
}

